package entity;

/**
 *
 * @author kmert
 */
public class Privilege {

    private int id;
    private int type;
    private String typeName;

    public Privilege() {
    }

    public Privilege(int id, int type, String typeName) {
        this.id = id;
        this.type = type;
        this.typeName = typeName;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getTypeName() {
        return typeName;
    }

    public void setTypeName(String typeName) {
        this.typeName = typeName;
    }


}
